package com.newgen.ws.request;

public class Attribute {
	
	private String Name;
	private String Value;
	public String getValue() {
		return Value;
	}
	public void setValue(String value) {
		Value = value;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	
	  
}
